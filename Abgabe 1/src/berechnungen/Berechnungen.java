package berechnungen;

public class Berechnungen {
    public static void main(String[] args) {
        System.out.println("Berechnungen:");
        System.out.println("3 - 1 = " + (3 - 1));
        System.out.println("5 * 2 = " + (5 * 2));
        System.out.println("5 + 2 = " + (5 + 2));
        System.out.println("5 / 2 = " + (5 / 2.0));
    }
}
